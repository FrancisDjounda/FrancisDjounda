package com.java.class3;

public class HomeworkRelationalOperator {

    public static void main(String[] args) {

        int a = 1 , b = -1;
        System.out.println(a > b);// Answer is true
        System.out.println(a < b);// Answer is false
        System.out.println(a >= b);// Answer is true
        System.out.println(a <= b);// Answer is false
        System.out.println(a != b);// Answer is true
        System.out.println(a == b);// Answer false




    }
}
