package com.java.class3;

public class HomeworkPriorityAssociativity {

    public static void main(String[] args) {
        // Expression Priority and Associativity

        System.out.println(12 + 8 / 3 + 4 / 2 - 3); // Answer is 13
        System.out.println(16 * 10 / (1 + 4) / 2 * 3);// Answer 48
        System.out.println(2 * 4 * (3 + 4) / (2 * 3));// Answer is 9
        System.out.println(5 * 2 / 10 + 6 / 6 * 6);// Answer is 7


    }
}